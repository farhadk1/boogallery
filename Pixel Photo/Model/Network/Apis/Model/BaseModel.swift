import Foundation
class BaseModel{
    struct SessionAndServerError: Codable {
        let code, status: String?
        let errors: Errors?
    }
    
    // MARK: - Errors
    struct Errors: Codable {
        let errorID, errorText: String?
        
        enum CodingKeys: String, CodingKey {
            case errorID = "error_id"
            case errorText = "error_text"
        }
    }

}
